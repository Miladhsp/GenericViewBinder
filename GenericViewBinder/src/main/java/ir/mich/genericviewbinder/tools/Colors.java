    public static int random() {
        Random rnd = new Random();
        return Color.argb(255,
                rnd.nextInt(256),
                rnd.nextInt(256),
                rnd.nextInt(256));
    }

    public static int random(boolean rnd_alpha) {
        Random rnd = new Random();
        return Color.argb(
                (rnd_alpha) ? rnd.nextInt(255) : 255,
                rnd.nextInt(256),
                rnd.nextInt(256),
                rnd.nextInt(256)
        );
    }

